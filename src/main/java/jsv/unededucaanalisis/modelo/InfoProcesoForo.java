package jsv.unededucaanalisis.modelo;

public class InfoProcesoForo 
{

	private String numRegistrosProcesados;
	private String numAsignaturas;
	private String numForos;
	private String numMensajes;
	private String numPersonas;

	public InfoProcesoForo() {}

	public String getNumRegistrosProcesados() {
		return numRegistrosProcesados;
	}

	public void setNumRegistrosProcesados(String numRegistrosProcesados) {
		this.numRegistrosProcesados = numRegistrosProcesados;
	}

	public String getNumAsignaturas() {
		return numAsignaturas;
	}

	public void setNumAsignaturas(String numAsignaturas) {
		this.numAsignaturas = numAsignaturas;
	}

	public String getNumForos() {
		return numForos;
	}

	public void setNumForos(String numForos) {
		this.numForos = numForos;
	}

	public String getNumMensajes() {
		return numMensajes;
	}

	public void setNumMensajes(String numMensajes) {
		this.numMensajes = numMensajes;
	}

	public String getNumPersonas() {
		return numPersonas;
	}

	public void setNumPersonas(String numPersonas) {
		this.numPersonas = numPersonas;
	}


	
}


